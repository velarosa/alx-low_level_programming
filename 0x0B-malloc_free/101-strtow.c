/**
 * wdcount - number of words
 * @s: given string
 * Description: find how many words
 *
 * Return: Always (size) Success.
 */

int wdcount(char *s)
{
	int i, n = 0;

	for (i = 0; s[i]; i++)
	{
		if (s[i] == ' ')
		{
			if (s[i + 1] != ' ' && s[i + 1] != '\0')
				n++;
		}
		else if (i == 0)
			n++;
	}
	n++;
	return (n);
}


